package overloading;

public class overloadingDemo {
	
	public void Sample() {
		
		System.out.println("Inside of method SampleOne");
		 
	}

	public void Sample(int a) {
		
		System.out.println("Inside sample one");
	}
	
	public void Sample(int a, int b) {
		
		System.out.println("Inside sample one");
		
	}
		
	public void Sample(int a, double b) {
		
		System.out.println("Inside sample one");
		
	}
	
	
	
}
